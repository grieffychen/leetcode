int bitwiseComplement(int n){
    int m = n;
    if(n == 0)
        return 1;
    int mask = 0;
    while(m){
        mask = ((mask<<1)|1);
        m = m >>1;
    }
    //printf("mask =%x\n",mask);
    return mask^n;
}


int main(int argc, char** argv)
{
    int num = atoi(argv[1]);
    int res =bitwiseComplement(num);
    printf("res=%d\n",res);
    return 0;
}

