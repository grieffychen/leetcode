int maxProfit(int* prices, int pricesSize) {
    //int max = 0;
    //int buy = prices[0];
    int earn = 0;
    for(int i = 1;i<pricesSize;i++){
        //int earn = 0;
        if((prices[i]-prices[i-1])>=0){
            earn+=prices[i]-prices[i-1];
        }
    }
    return earn;
}

