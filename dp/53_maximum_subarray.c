int maxSubArray(int* nums, int numsSize) {
    int maxnum = INT_MIN;
    int sum = 0;
    for(int i =0;i<numsSize;i++){
        sum+=nums[i];
        if(sum<nums[i])
            sum = nums[i];
        maxnum = maxnum>sum?maxnum:sum;
    }
    return maxnum;
}

int main(int argc, char** argv)
{
    int nums[] = {-2,1,-3,4,-1,2,1,-5,4};
    int max = maxSubArray(nums,sizeof(nums)/sizeof(int));
    printf("max=%d\n",max);

    return 0;
}