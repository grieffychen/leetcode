int findComplement(int num){

    int val = num;
    int bcnt = 0;
    while(val){
        bcnt++;
        val>>=1;
    }
    printf("bit cnt=%d\n",bcnt);
    uint32_t mask = ((uint32_t)1<<bcnt)-1;
    printf("mask =%x\n",mask);
    return num^mask;
}
int main(int argc, char** argv)
{
    int num = atoi(argv[1]);
    int comp_num = findComplement(num);
    printf("the comp num =%d\n",comp_num);
    return 0;
}

